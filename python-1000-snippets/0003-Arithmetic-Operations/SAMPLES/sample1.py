# sample1.py
# basic arithmetic demonstration

def main():
    a = 12
    b = 5
    print("a + b =", a + b)
    print("a - b =", a - b)
    print("a * b =", a * b)
    print("a / b =", a / b)
    print("a // b =", a // b)
    print("a % b =", a % b)
    print("a ** b =", a ** b)

if __name__ == '__main__':
    main()

